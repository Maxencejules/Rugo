/* Uninitialized -> .bss; must read back zero across multiple pages. */
static unsigned int bssbuf[BIGN];

/* A function reached only by address, to exercise mingw .refptr
 * indirection surviving the PE->ELF rewrap. */
static unsigned int fold(unsigned int a, unsigned int b) {
    return (a ^ (b + 0x9E3779B9u + (a << 6) + (a >> 2)));
}

static unsigned int (*const fold_ptr)(unsigned int, unsigned int) = fold;

int main(void) {
    unsigned int sum = 0;
    for (int i = 0; i < BIGN; i++) sum += bigtab[i];
    unsigned int high = bigtab[BIG_HIGH];

    unsigned int bsum = 0;
    for (int i = 0; i < BIGN; i++) bsum += bssbuf[i];
    /* write+read the high (3rd+ page) BSS slot */
    bssbuf[BIG_HIGH] = 0xA5A5A5A5u;
    unsigned int bhi = bssbuf[BIG_HIGH];

    unsigned int f = fold_ptr(high, sum);
    unsigned int fchk = fold(high, sum);

    if (sum != EXPECT_SUM || high != EXPECT_HIGH ||
        bsum != 0 || bhi != 0xA5A5A5A5u || f != fchk) {
        printf("BIGC: FAIL sum=0x%x high=0x%x bsum=0x%x bhi=0x%x f=0x%x fchk=0x%x\n",
               (unsigned long)sum, (unsigned long)high, (unsigned long)bsum,
               (unsigned long)bhi, (unsigned long)f, (unsigned long)fchk);
        return 1;
    }
    printf("BIGC: ok sum=0x%x high=0x%x pages>2\n",
           (unsigned long)sum, (unsigned long)high);

    /* rlibc v2/v3, exercised here on the dedicated disk (kept off the
     * size-tight shared app region): real free() with block reuse, and a
     * bidirectional buffered FILE* round-trip. */
    void *h1 = malloc(48);
    free(h1);
    void *h2 = malloc(48);
    void *h3 = malloc(48);
    printf("BIGC: heap reuse=%d distinct=%d\n", (long)(h2 == h1),
           (long)(h3 != h1 && h3 != h2));

    FILE *wf = fopen("/data/bigc.txt", "w");
    if (wf) {
        fwrite("rlibc-stdio", 1, 11, wf);
        fclose(wf);
        FILE *rf = fopen("/data/bigc.txt", "r");
        if (rf) {
            char rb[16];
            size_t g = fread(rb, 1, sizeof(rb) - 1, rf);
            rb[g] = 0;
            int at_eof = feof(rf);
            fclose(rf);
            printf("BIGC: stdio rw[%d]=%s eof=%d\n", (long)g, rb, (long)at_eof);
        } else {
            puts("BIGC: stdio reopen err");
        }
    } else {
        puts("BIGC: stdio fopen err");
    }

    /* rlibc v2 DISTINCT errno (Part V.11): two different failures, routed through
     * the libc open()/read() wrappers, now carry two different errno values
     * (ENOENT vs EBADF) instead of a single EIO -- the wrappers read sys_errno. */
    errno = 0;
    (void)open("/data/nope", O_RDONLY, 0); /* missing file -> ENOENT */
    int e_open = errno;
    errno = 0;
    char eb[1];
    (void)read(99, eb, 1); /* out-of-range fd -> EBADF */
    int e_read = errno;
    printf("BIGC: errno enoent=%d ebadf=%d distinct=%d\n",
           (long)(e_open == ENOENT), (long)(e_read == EBADF),
           (long)(e_open != e_read));

    puts("BIGC: done");
    return 0;
}
